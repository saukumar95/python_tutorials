def addition(x, y):
    # results: int =
    return x + y


def subtraction(x, y):
    # results: int =
    return x - y


a = int(input('Enter first number: '))
b = int(input('Enter second number: '))

print('Result is: ' + str(int(addition(a, b))))
print('Result is: ' + str(int(subtraction(a, b))))
